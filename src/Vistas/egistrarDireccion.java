    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String url = "jdbc:mysql://localhost:3307/proyecto";
        String user = "root";
        String pass = "";

        try
        {
            Connection cn = DriverManager.getConnection(url, user, pass);

            
            PreparedStatement pst = (PreparedStatement) cn.prepareStatement("insert into direccion (id,Av,Calle,Cod_centro) values(NULL,?,?,?)");

            if (cn != null)
            {
                System.out.println("Conexion correcta");
            } else
            {
                System.out.println("Conexion fallida");
            }

            pst.setString(1, txtAvenida.getText());
            pst.setString(2, txtCalle.getText());
            pst.setInt(3, Integer.parseInt(txtCodCentro.getText()));
            int res=pst.executeUpdate();

            txtAvenida.setText(null);
            txtCalle.setText(null);

            if (res>0)
            {
                JOptionPane.showMessageDialog(null, "Registro exitoso!!");
            } else
            {
                JOptionPane.showMessageDialog(null, "Algo salio mal!!");
            }

        } catch (SQLException ex)
        {
            System.out.println( "ERROR " + ex.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

